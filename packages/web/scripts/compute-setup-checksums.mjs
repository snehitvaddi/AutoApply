#!/usr/bin/env node
/**
 * Compute SHA256 hashes of the setup scripts in public/setup/ and emit a
 * TypeScript module at src/generated/setup-checksums.ts so the /setup-complete
 * page can display the expected hash alongside the curl-pipe-bash command.
 *
 * This defends against DNS poisoning / CDN compromise / domain takeover: a
 * user who pastes our documented command can independently verify the
 * downloaded script matches the hash shown on applyloop.vercel.app before
 * executing it. (The recommended command in the UI uses a two-step
 * download → verify → run flow, not the raw curl | bash.)
 *
 * Runs as a "prebuild" step before `next build`. Safe to run locally too —
 * idempotent, only writes the file when hashes change.
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { createHash } from "node:crypto";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = join(__dirname, "..");

const FILES = [
  { key: "mac", path: "public/setup/ApplyLoop-Setup-Mac.sh" },
  { key: "windowsPs1", path: "public/setup/ApplyLoop-Setup-Windows.ps1" },
  { key: "windowsBat", path: "public/setup/ApplyLoop.bat" },
];

function sha256(buf) {
  return createHash("sha256").update(buf).digest("hex");
}

const entries = FILES.map(({ key, path }) => {
  const abs = join(WEB_ROOT, path);
  if (!existsSync(abs)) {
    console.warn(`[checksums] missing ${path} — emitting empty hash`);
    return { key, path, hash: "" };
  }
  const hash = sha256(readFileSync(abs));
  return { key, path, hash };
});

const outDir = join(WEB_ROOT, "src", "generated");
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });

const body = `// AUTO-GENERATED by scripts/compute-setup-checksums.mjs — do not edit by hand.
// Regenerated on every build via the "prebuild" npm script.

export interface SetupChecksum {
  path: string;
  sha256: string;
}

export const SETUP_CHECKSUMS: Record<"mac" | "windowsPs1" | "windowsBat", SetupChecksum> = {
${entries
  .map(
    (e) =>
      `  ${e.key}: { path: ${JSON.stringify(e.path)}, sha256: ${JSON.stringify(
        e.hash
      )} },`
  )
  .join("\n")}
};

export const SETUP_CHECKSUMS_GENERATED_AT = ${JSON.stringify(new Date().toISOString())};
`;

const outFile = join(outDir, "setup-checksums.ts");
const prev = existsSync(outFile) ? readFileSync(outFile, "utf8") : "";
if (prev !== body) {
  writeFileSync(outFile, body);
  console.log(`[checksums] wrote ${outFile}`);
  for (const e of entries) {
    console.log(`  ${e.key.padEnd(11)} ${e.hash || "(missing)"}  ${e.path}`);
  }
} else {
  console.log(`[checksums] unchanged — ${outFile}`);
}
