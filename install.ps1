<#
.SYNOPSIS
    ApplyLoop installer for Windows 10/11.

.DESCRIPTION
    PowerShell port of install.sh. Handles activation, dependency bootstrap
    (winget for Python/Node/Git, npm for openclaw + claude-code), repo clone,
    venv setup, UI build, cloud config fetch, profile.json + .env generation,
    .exe build, CLI shim, and Task Scheduler auto-update job.

.USAGE
    Recommended one-liner (run in an elevated PowerShell window):
        irm https://applyloop.vercel.app/install.ps1 | iex

    With activation code via env var:
        $env:APPLYLOOP_CODE = "AL-XXXX-XXXX"; irm https://applyloop.vercel.app/install.ps1 | iex

    Or download and run manually:
        iwr https://applyloop.vercel.app/install.ps1 -OutFile install.ps1
        .\install.ps1 -Code AL-XXXX-XXXX

.NOTES
    Requires Windows 10 1809+ (for ConPTY/winget).
    Mirrors install.sh feature-for-feature where possible.
#>
[CmdletBinding()]
param(
    [string]$Code = "",
    [string]$Branch = "main",
    [string]$RepoUrl = "https://github.com/snehitvaddi/ApplyLoop",
    # Skip the full install path; only regenerate the CLI shim + fallback
    # aliases from the already-cloned repo. Used by update.ps1 to self-heal
    # an older or broken applyloop.cmd without re-running the full install.
    [switch]$ShimRefreshOnly
)

$ErrorActionPreference = "Stop"

# ─── Logging helpers ────────────────────────────────────────────────────────
function Write-Log    ($msg) { Write-Host "[applyloop] $msg" -ForegroundColor Cyan }
function Write-Warn   ($msg) { Write-Host "[applyloop] WARN: $msg" -ForegroundColor Yellow }
function Write-Err    ($msg) { Write-Host "[applyloop] ERROR: $msg" -ForegroundColor Red }
function Die          ($msg) { Write-Err $msg; exit 1 }

# ─── BOM-less UTF-8 file writer ─────────────────────────────────────────────
# Windows PowerShell 5.1's `Set-Content -Encoding UTF8` writes files with a
# 3-byte UTF-8 BOM (EF BB BF). Python reads on Windows default to cp1252,
# which decodes those BOM bytes as 3 high-ordinal characters and then chokes
# when the value gets passed to httpx as an HTTP header value or to json.load.
# Use the .NET UTF8Encoding with $false (no BOM) to match Mac install.sh.
function Write-Utf8NoBom {
    param(
        [Parameter(Mandatory=$true)] [string]$Path,
        [Parameter(Mandatory=$true)] [string]$Content
    )
    $utf8NoBom = New-Object System.Text.UTF8Encoding $false
    [System.IO.File]::WriteAllText($Path, $Content, $utf8NoBom)
}

# ─── Native-command runner immune to PowerShell's stderr-is-fatal trap ─────
# With $ErrorActionPreference = "Stop", PowerShell escalates ANY stderr
# output from a native command (npm warnings, schtasks chatter, deprecation
# notices) into a terminating exception — even when stderr is redirected
# with 2>&1 | Out-Null. cmd /c bypasses this because the called process's
# stderr stays inside cmd's process boundary and never crosses back into
# PowerShell's error stream. Use this wrapper for any external CLI whose
# stderr is chatty but non-fatal (npm, schtasks, winget warnings, etc.).
function Invoke-Native {
    param(
        [Parameter(Mandatory=$true)] [string]$Command,
        [string]$Description = ""
    )
    if ($Description) { Write-Log $Description }
    cmd /c $Command 2>$null | Out-Null
    return $LASTEXITCODE
}

