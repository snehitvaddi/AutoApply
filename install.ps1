# Stops Task Scheduler entry, removes installed code/binaries/shim, removes API
# token from workspace. PRESERVES the workspace (~/.autoapply) so user keeps
# their applications.db, resumes, screenshots — re-installing later picks
# right back up where they left off.
$UninstallScript = Join-Path $ApplyloopHome "uninstall.ps1"
$UninstallScriptContent = @"
# ApplyLoop uninstaller — generated by install.ps1.
# Removes: Task Scheduler entry, install dir, .exe + shim, API token.
# Preserves: ~/.autoapply workspace (db, resumes, screenshots) so a
# future reinstall keeps your job history. Run with `applyloop uninstall`.
[CmdletBinding()] param([switch]`$Force)
if (-not `$Force) {
    Write-Host "ApplyLoop uninstall — this removes:"
    Write-Host "  - Daily auto-update task (Task Scheduler)"
    Write-Host "  - $ApplyloopHome (source, venv, .env, profile.json)"
    Write-Host "  - $LocalBinDir (the .exe + applyloop.cmd shim)"
    Write-Host "  - %USERPROFILE%\.autoapply\workspace\.api-token"
    Write-Host ""
    Write-Host "PRESERVES: ~/.autoapply (your job database, resumes, screenshots)."
    Write-Host "Pass -Force to skip this prompt."
    `$reply = Read-Host "Continue? (y/N)"
    if (`$reply -notmatch '^[Yy]') { Write-Host "Cancelled."; exit 0 }
}
Write-Host "[uninstall] Removing Task Scheduler entry..."
schtasks /Delete /TN ApplyLoopUpdate /F 2>&1 | Out-Null
Write-Host "[uninstall] Removing installed code at $ApplyloopHome..."
Remove-Item -Recurse -Force '$ApplyloopHome' -ErrorAction SilentlyContinue
Write-Host "[uninstall] Removing .exe + shim at $LocalBinDir..."
Remove-Item -Recurse -Force '$LocalBinDir' -ErrorAction SilentlyContinue
Write-Host "[uninstall] Removing Start Menu + Desktop shortcuts..."
`$startLnk   = Join-Path `$env:APPDATA 'Microsoft\Windows\Start Menu\Programs\ApplyLoop.lnk'
`$desktopLnk = Join-Path ([System.Environment]::GetFolderPath('Desktop')) 'ApplyLoop.lnk'
Remove-Item `$startLnk, `$desktopLnk -Force -ErrorAction SilentlyContinue
`$tokenFile = Join-Path `$env:USERPROFILE '.autoapply\workspace\.api-token'
if (Test-Path `$tokenFile) {
    Write-Host "[uninstall] Removing API token..."
    Remove-Item `$tokenFile -ErrorAction SilentlyContinue
}
Write-Host ""
Write-Host "Uninstalled. Workspace preserved at %USERPROFILE%\.autoapply"
Write-Host "(applications.db, resumes/, screenshots/ — delete manually if you want a clean slate)"
Write-Host ""
Write-Host "Optional: also uninstall global npm packages:"
Write-Host "  npm uninstall -g openclaw"
Write-Host "  npm uninstall -g @anthropic-ai/claude-code"
"@
Write-Utf8NoBom -Path $UninstallScript -Content $UninstallScriptContent
Write-Log "Uninstall script → $UninstallScript"

# Shim that dispatches on first arg:
#   applyloop          → launch the .exe (default)
#   applyloop start    → launch the .exe
#   applyloop update   → run update.ps1
#   applyloop uninstall → run uninstall.ps1 (interactive prompt)
#   applyloop version  → cat .applyloop-version
if ($ExePath) {
    $exeFullPath = $ExePath.FullName
    Write-ApplyloopShim `
        -LocalBinDir $LocalBinDir `
        -ApplyloopHome $ApplyloopHome `
        -ExePath $exeFullPath `
        -UpdateScript $UpdateScript `
        -UninstallScript $UninstallScript
    Write-Log "CLI shim → $LocalBinDir\applyloop.cmd (+ applyloop-update.cmd, applyloop-uninstall.cmd)"

    # ─── Start Menu + Desktop shortcuts ─────────────────────────────────
    # Without these, the user has to dig through %LOCALAPPDATA%\Programs to
    # find ApplyLoop.exe — there's no Win-key-then-type-ApplyLoop. Create
    # a .lnk so the Start menu indexes it and a desktop shortcut so first-
    # time users see exactly the same "click an icon to launch" affordance
    # they'd get on macOS from /Applications/ApplyLoop.app.
    try {
        $iconCandidate = Join-Path $ApplyloopHome "packages\desktop\icon.ico"
        if (-not (Test-Path $iconCandidate)) {
            $iconCandidate = $exeFullPath  # PyInstaller embeds the .exe icon
        }
        $startMenuDir  = Join-Path $env:APPDATA "Microsoft\Windows\Start Menu\Programs"
        $startLnk      = Join-Path $startMenuDir "ApplyLoop.lnk"
        $desktopLnk    = Join-Path ([System.Environment]::GetFolderPath("Desktop")) "ApplyLoop.lnk"
        $wsh           = New-Object -ComObject WScript.Shell
        foreach ($lnk in @($startLnk, $desktopLnk)) {
            $shortcut                  = $wsh.CreateShortcut($lnk)
            $shortcut.TargetPath       = $exeFullPath
            $shortcut.WorkingDirectory = Split-Path $exeFullPath -Parent
            $shortcut.IconLocation     = $iconCandidate
            $shortcut.Description      = "ApplyLoop — automated job-application desktop app"
            $shortcut.Save()
        }
        Write-Log "Start Menu + Desktop shortcuts created (search for 'ApplyLoop' from Start)"
    } catch {
        Write-Warn "Could not create Start Menu/Desktop shortcut: $_"
    }
} else {
    Write-Warn ".exe build did not produce ApplyLoop.exe at expected path; CLI shim skipped"
}

# Add LocalBinDir to user PATH (idempotent)
$userPath = [System.Environment]::GetEnvironmentVariable("PATH", "User")
if (-not ($userPath -split ";" | Where-Object { $_ -eq $LocalBinDir })) {
    Write-Log "Adding $LocalBinDir to user PATH"
    $newPath = if ($userPath) { "$userPath;$LocalBinDir" } else { $LocalBinDir }
    [System.Environment]::SetEnvironmentVariable("PATH", $newPath, "User")
    $env:PATH = "$env:PATH;$LocalBinDir"
}

# Version stamp
$version = git -C $ApplyloopHome rev-parse HEAD 2>$null
if ($version) {
    Set-Content -Path (Join-Path $ApplyloopHome ".applyloop-version") -Value $version -Encoding UTF8
}

# ─── Phase K: Auto-update Task Scheduler ───────────────────────────────────
# Point the daily task DIRECTLY at update.ps1, not at the shim. The shim
# would also work but adds an extra cmd.exe → powershell.exe hop. Direct
# is one less moving part.
Write-Log "Registering daily auto-update task (3 AM)"
# Wrap the whole block in a local SilentlyContinue + try/catch so a schtasks
# failure (e.g. non-elevated shell → "Access is denied" on the system-scope
# task store) doesn't terminate the install. The install itself is complete
# by this point — .exe built, shortcuts placed, CLI shim wired. The auto-
# update task is a nice-to-have; the user can always rerun the install or
# manually run `applyloop update` to grab new versions.
$prevEAP = $ErrorActionPreference
$ErrorActionPreference = "SilentlyContinue"
$taskRegistered = $false
try {
    cmd /c "schtasks /Delete /TN ApplyLoopUpdate /F" 2>$null | Out-Null
    $updateCmd = "powershell.exe -NoProfile -ExecutionPolicy Bypass -File `"$UpdateScript`""
    # cmd /c bypasses PowerShell's "stderr → terminating error" coupling
    # under $ErrorActionPreference=Stop, so schtasks's normal stderr chatter
    # never trips the install. We rely on the exit code for the truth.
    cmd /c "schtasks /Create /TN ApplyLoopUpdate /TR `"$updateCmd`" /SC DAILY /ST 03:00 /F" 2>$null | Out-Null
    if ($LASTEXITCODE -eq 0) { $taskRegistered = $true }
} catch {
    # Intentionally swallow; we report via $taskRegistered below.
}
$ErrorActionPreference = $prevEAP
if ($taskRegistered) {
    Write-Log "  Daily 3 AM update task registered → $UpdateScript"
} else {
    Write-Warn "  Task Scheduler registration skipped (likely non-elevated shell)."
    Write-Warn "  Auto-updates won't run nightly, but everything else works."
    Write-Warn "  To enable: open PowerShell as Administrator, then run:"
    Write-Warn "    schtasks /Create /TN ApplyLoopUpdate /TR `"powershell.exe -NoProfile -ExecutionPolicy Bypass -File '$UpdateScript'`" /SC DAILY /ST 03:00 /F"
    Write-Warn "  Or just run 'applyloop update' manually whenever you want the latest version."
}

