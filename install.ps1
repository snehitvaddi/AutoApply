# ─── Shim-refresh-only fast path ───────────────────────────────────────────
# Used by update.ps1 to regenerate the CLI shim + fallback aliases without
# re-running the full install. We embed the shim-writing logic in a function
# so both the full install path AND this fast path call it identically.
function Write-ApplyloopShim {
    param(
        [Parameter(Mandatory=$true)] [string]$LocalBinDir,
        [Parameter(Mandatory=$true)] [string]$ApplyloopHome,
        [Parameter(Mandatory=$true)] [string]$ExePath,
        [Parameter(Mandatory=$true)] [string]$UpdateScript,
        [Parameter(Mandatory=$true)] [string]$UninstallScript
    )
    New-Item -ItemType Directory -Force -Path $LocalBinDir | Out-Null
    $ShimPath = Join-Path $LocalBinDir "applyloop.cmd"
    # IF /I is the cmd.exe case-insensitive comparator. Without it,
    # `applyloop Update` and `applyloop UPDATE` fall through to the
    # default branch and launch the app instead of pulling latest.
    $shim = @"
@echo off
REM ApplyLoop CLI shim — generated by install.ps1
setlocal
if /I "%~1" == "update" (
    powershell.exe -NoProfile -ExecutionPolicy Bypass -File "$UpdateScript"
    exit /b %ERRORLEVEL%
)
if /I "%~1" == "uninstall" (
    powershell.exe -NoProfile -ExecutionPolicy Bypass -File "$UninstallScript" %2 %3
    exit /b %ERRORLEVEL%
)
if /I "%~1" == "version" (
    type "$ApplyloopHome\.applyloop-version"
    exit /b 0
)
if /I "%~1" == "status" (
    powershell.exe -NoProfile -Command "try { Invoke-RestMethod 'http://localhost:18790/api/health' | ConvertTo-Json } catch { Write-Host 'ApplyLoop is not running.'; exit 1 }"
    exit /b %ERRORLEVEL%
)
if /I "%~1" == "stop" (
    powershell.exe -NoProfile -Command "Get-Process -Name 'ApplyLoop' -ErrorAction SilentlyContinue | Stop-Process -Force -ErrorAction SilentlyContinue; Write-Host 'Stopped.'"
    exit /b 0
)
if /I "%~1" == "logs" (
    powershell.exe -NoProfile -Command "Get-Content '%USERPROFILE%\.autoapply\desktop.log' -Tail 100 -Wait"
    exit /b 0
)
if /I "%~1" == "help"   goto :usage
if /I "%~1" == "-h"     goto :usage
if /I "%~1" == "--help" goto :usage
if /I "%~1" == "start" (
    start "" "$ExePath"
    exit /b 0
)
if "%~1" == "" (
    start "" "$ExePath"
    exit /b 0
)
echo Unknown command: %~1
echo.
:usage
echo Usage: applyloop ^<command^>
echo.
echo   start         Launch the desktop app (default if no command)
echo   stop          Kill any running ApplyLoop.exe
echo   status        Check /api/health
echo   logs          Tail ~/.autoapply/desktop.log
echo   update        Pull latest + rebuild .exe
echo   uninstall     Remove install + .exe + shortcuts + Task Scheduler entry
echo   version       Print current commit
echo   help          Show this message
exit /b 1
"@
    Set-Content -Path $ShimPath -Value $shim -Encoding ASCII
    # Standalone fallback aliases — survive any future dispatcher bug.
    Set-Content -Path (Join-Path $LocalBinDir "applyloop-update.cmd") -Encoding ASCII -Value @"
@echo off
powershell.exe -NoProfile -ExecutionPolicy Bypass -File "$UpdateScript" %*
"@
    Set-Content -Path (Join-Path $LocalBinDir "applyloop-uninstall.cmd") -Encoding ASCII -Value @"
@echo off
powershell.exe -NoProfile -ExecutionPolicy Bypass -File "$UninstallScript" %*
"@
}

if ($ShimRefreshOnly) {
    Write-Log "Shim-refresh-only mode: regenerating CLI shim + aliases"
    $exeRel = Get-ChildItem -Path (Join-Path $ApplyloopHome "packages\desktop\dist\windows") `
        -Recurse -Filter "ApplyLoop.exe" -ErrorAction SilentlyContinue | Select-Object -First 1
    if (-not $exeRel) { Die "No ApplyLoop.exe found under $ApplyloopHome — run a full install first." }
    Write-ApplyloopShim `
        -LocalBinDir $LocalBinDir `
        -ApplyloopHome $ApplyloopHome `
        -ExePath $exeRel.FullName `
        -UpdateScript (Join-Path $ApplyloopHome "update.ps1") `
        -UninstallScript (Join-Path $ApplyloopHome "uninstall.ps1")
    Write-Log "Shim refreshed → $LocalBinDir\applyloop.cmd (+ applyloop-update.cmd, applyloop-uninstall.cmd)"
    exit 0
}

